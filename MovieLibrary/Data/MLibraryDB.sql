 CREATE TABLE Accounts
 (
	Id INT IDENTITY PRIMARY KEY,
	FirstName VARCHAR(50) NOT NULL,
	LastName VARCHAR(50) NOT NULL,
	Username VARCHAR(25) NOT NULL,
	Password VARCHAR(50) NOT NULL,
	RecoveryKey INT NOT NULL
 );
